export interface BoundaryFeature {
  type: 'Feature'
  properties: {
    id: string
    name: string
    level: 'province' | 'city' | 'basin'
    color: string
    area?: string
    description?: string
    center?: [number, number]
  }
  geometry: {
    type: 'Polygon'
    coordinates: number[][][]
  }
}

export interface BoundaryGeoJSON {
  type: 'FeatureCollection'
  features: BoundaryFeature[]
}

